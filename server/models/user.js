const mongoose = require("mongoose");

const USerSchema = new mongoose.Schema(
  {
    username: {
      type: String,
      required: true,
      max: 50,
      unique: true,
    },
    email: {
      type: String,
      required: true,
      max: 50,
      unique: true,
    },
    password: {
      type: String,
      required: true,
      min: 6,
    }
  },
  { timestamps: true }
);
module.exports = mongoose.model("users", USerSchema);
